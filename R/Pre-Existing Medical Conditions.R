                      col == 'S.06',    'History of PVD',
                      col == 'S.07',    'Prematurity',
                      col == 'S.08',    'Pre-hospital Cardiac Arrest',
                      col == 'T.00',    'Congenital Disorder',
                      default =          'Not Documented' )
