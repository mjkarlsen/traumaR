                      col == 'J.05',    'Chronic Demyelinating Disease',
                      col == 'J.06',    'Chronic Dementia',
                      col == 'J.07',    'Organic Brain Syndrome',
                      col == 'J.08',    'Parkinsons Disease',
                      col == 'J.09',    'CVA',
                      col == 'J.10',    'Autism Spectrum',
